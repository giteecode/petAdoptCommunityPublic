      @pagination="getList"
    />

    <!-- 添加或修改小程序用户对话框 -->
    <el-dialog :title="title" :visible.sync="open" width="500px" append-to-body>
      <el-form ref="form" :model="form" :rules="rules" label-width="80px">
        <el-form-item label="用户ID" prop="openId">
          <el-input v-model="form.openId" placeholder="请输入用户ID" />
        </el-form-item>
        <el-form-item label="session_key" prop="sessionKey">
          <el-input v-model="form.sessionKey" placeholder="请输入session_key" />
        </el-form-item>
        <el-form-item label="union_id" prop="unionId">
          <el-input v-model="form.unionId" placeholder="请输入union_id" />
        </el-form-item>
        <el-form-item label="用户昵称" prop="nickName">
          <el-input v-model="form.nickName" placeholder="请输入用户昵称" />
